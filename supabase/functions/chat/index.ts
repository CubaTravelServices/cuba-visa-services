import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers":
    "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          {
            role: "system",
            content: `You are a friendly and knowledgeable customer support assistant for Cuba Visa Services, America's most trusted Cuba visa provider since 1999. You help travelers with questions about:

- Cuba E-Visa and Tourist Card processing
- D'Viajeros travel form assistance
- Application status and processing times
- Travel requirements for US citizens visiting Cuba
- OFAC authorized travel categories
- Airport services at Miami (MIA) and Tampa (TPA)
- Pricing and service packages (Standard, Priority, Rush)

Key facts:
- Cuba Visa Services operates under a direct contract with the Cuban Consulate in Washington, D.C. and US-based airlines
- IATA Accredited, OFAC Authorized, Florida Seller of Travel
- Physical staff at MIA and TPA airports
- Cuba Visa Services is a Cuba Travel Services affiliate
- Customer support phone: 1-800-963-2822
- Email: Info@CubaVisaServices.com

Be concise, helpful, and professional. If someone needs to apply, direct them to the Apply Now page. For complex issues, suggest calling 1-800-963-2822 or emailing Info@CubaVisaServices.com.`,
          },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "We're experiencing high demand. Please try again in a moment." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "Service temporarily unavailable. Please call 1-800-963-2822." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "Something went wrong. Please try again." }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
